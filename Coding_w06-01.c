#include <stdio.h>

int main() {
    // กำหนดค่าตัวแปร
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    printf("ค่าเริ่มต้น: a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d);        // บรรทัดที่ 1: แสดงค่าเริ่มต้นของตัวแปรทั้งหมด
    printf("ผลการคำนวณของ d + a = %.1f\n", d + a);  // 4.0 + 5 = 9.0           // บรรทัดที่ 2: ผลการคำนวณของ d + a
    printf("ผลการคำนวณของ a - b = %d\n", a - b);    // 5 - 17 = -12            // บรรทัดที่ 3: ผลการคำนวณของ a - b
    printf("ผลการคำนวณของ c * d = %.2f\n", c * d);  // 8.5 * 4.0 = 34.0        // บรรทัดที่ 4: ผลการคำนวณของ c * d
    printf("ผลการคำนวณของ a * c = %.2f\n", a * c);  // 5 * 8.5 = 42.5          // บรรทัดที่ 5: ผลการคำนวณของ a * c
    printf("ผลการคำนวณของ c / d = %.3f\n", c / d);  // 8.5 / 4.0 = 2.125       // บรรทัดที่ 6: ผลการคำนวณของ c / d
    printf("ผลการคำนวณของ b / c = %.2f\n", b / c);  // 17 / 8.5 = 2.0          // บรรทัดที่ 7: ผลการคำนวณของ b / c
    printf("ผลการคำนวณของ a %% b = %d\n", a % b);   // 5 % 17 = 5              // บรรทัดที่ 8: ผลการคำนวณของ a %% b
    printf("ผลการคำนวณของ (int)c %% a = %d\n", ((int)c) % a);  // 8 % 5 = 3    // บรรทัดที่ 9: ผลการคำนวณของ (int)c %% a
    printf("ผลการคำนวณของ (int)c %% (int)d = %d\n", ((int)c) % ((int)d));      // 8 % 4 = 0   // บรรทัดที่ 10: ผลการคำนวณของ (int)c %% (int)d

    return 0;
}



